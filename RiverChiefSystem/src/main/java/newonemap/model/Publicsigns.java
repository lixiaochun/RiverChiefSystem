package newonemap.model;

public class Publicsigns {
	
	String name;
	String regionName;
	String riverchief;
	String rivemanager;
	String duty;
	String target;
	String hotline;
	String imgurl;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRegionName() {
		return regionName;
	}
	public void setRegionName(String regionName) {
		this.regionName = regionName;
	}
	public String getRiverchief() {
		return riverchief;
	}
	public void setRiverchief(String riverchief) {
		this.riverchief = riverchief;
	}
	public String getRivemanager() {
		return rivemanager;
	}
	public void setRivemanager(String rivemanager) {
		this.rivemanager = rivemanager;
	}
	public String getDuty() {
		return duty;
	}
	public void setDuty(String duty) {
		this.duty = duty;
	}
	public String getTarget() {
		return target;
	}
	public void setTarget(String target) {
		this.target = target;
	}
	public String getHotline() {
		return hotline;
	}
	public void setHotline(String hotline) {
		this.hotline = hotline;
	}
	public String getImgurl() {
		return imgurl;
	}
	public void setImgurl(String imgurl) {
		this.imgurl = imgurl;
	}
	
	

}
